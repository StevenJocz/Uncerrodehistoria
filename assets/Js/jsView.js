
// ========= Menu =============== 
function MenuLeftDos(valor) {
    switch (valor) {
        case 1:
            window.location.href = "/4toRadio/";
            break;
        case 2:
            window.location.href = "/4toRadio/#Noticias"; 
            break;
        case 3:
            window.location.href = "/4toRadio/#Playlist"; 
            break;
        case 4:
            window.location.href = "/4toRadio/#Programacion"; 
            break;
        case 5:
            window.location.href = "/4toRadio/#Nosotros"; 
            break;
    }
}

